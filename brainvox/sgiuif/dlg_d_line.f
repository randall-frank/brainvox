	Subroutine dlg_d_line(item)

	Implicit None

	Include 'fgl.h'
	Include 'fdevice.h'
	Include 'sgiuif.h'  
	Include 'sgiuif.f'  

	record	/ditem/	item
	
	If (RGBmode) then
		Call c3i(dlg_colors(1,1))
	Else
		Call color(cbase+1)
	Endif

	Call bgnlin
	Call v2i(item.rect(1))   !x1,y1
	Call v2i(item.rect(3))	 !x2,y2
	Call endlin
	
	Return
	End
